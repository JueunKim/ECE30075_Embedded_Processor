	AREA CodeSection, CODE, READONLY
	THUMB
	EXPORT sumofN

sumofN PROC
	SUBS R0,R0,#2	
	BNE ADD1
	ADD R1, R1, R2
	ADD R0, R1, R3
	B BACK

ADD1
	SUBS R0,R0,#1				
	BNE ADD2
	ADD R1,R1,R2
	ADD R0,R1,R3
	B BACK
					
ADD2
	ADD R1,R1,R2
	ADD R1,R1,R3
	B LOOP

LOOP
	SUBS R0,R0,#1
	LDMFD SP!,{R2}
	ADD R1,R1,R2
	BNE LOOP
	MOV R0,R1
	B BACK


BACK
	BX LR

END

